{
	volatile unsigned long delay;
  SIM->SCGC4 |= (1 << 22);//activate SSI0
  SIM->SCGC5 |= (1 <<  11);//activate Port C
	
  PORTC->PCR[7] |= (1 << 8);//configure PTC7(DC) as GPIO(ALT1) 
  PORTC->PCR[8] |= (1 << 8);//configure PTC8(RST) as GPIO(ALT1)
  GPIOC->PDDR |= (1 << 7)|(1 << 8);//set PTC7(DC),PTC8(RST) as output pins 
  
  PORTC->PCR[4] |= (0x2 << 8);//configure PTC4 as SPI SS(ALT2) 
  PORTC->PCR[5] |= (0x2 << 8);//configure PTC5 as SPI Clock(ALT2)
  PORTC->PCR[6] |= (0x2 << 8);//configure PTC6 as SPI MOSI(ALT2)
  
  SPI0->C1 &= ~(1 << 6);//disable SSI 
  SPI0->C1 |= (1 << 4);//master mode
  SPI0->C1 &= ~(1 << 0);//SPI serial data transfers start with MSB
  SPI0->C1 &= ~(1 << 3);//CPOL=0
  SPI0->C1 &= ~(1 << 2);//CPHA=0
  /*
  SSOE, MODFEN=1 --> SS pin (of Master) used as Slave Select Output
  */
  SPI0->C1 |= (1 << 1);//SSOE=1
  SPI0->C2 |= (1 << 4);//MODFEN=1
  /*
  BRD = (SPPR + 1) * 2^(SPR + 1)
  SPPR=0
  SPR=2
  BRD = 1 * 2^3 = 8
  Baud Rate = Bus Clock Freq./BRD = 24 MHz (default)/8 = 3 MHz
  */
  SPI0->BR &= ~(0x7 << 4);//SPPR=0
  SPI0->BR |= (0x2 << 0);//SPR=2
  SPI0->C1 |= (1 << 6);//enable SSI

  GPIOC->PDOR &= ~(1 << 8);// hold RST(PTC8) pin LOW
  for(delay=0; delay<5; delay++);// delay minimum 100 ns
  GPIOC->PDOR |= (1 << 8);// set RST(PTC8) pin HIGH

  lcdwrite(COMMAND, 0x21);              // chip active(PD=0); horizontal addressing mode(V = 0); use extended instruction set(H = 1)
  lcdwrite(COMMAND, 0x90);
  lcdwrite(COMMAND, CONTRAST);          // try 0xB8
  lcdwrite(COMMAND, 0x04);              // set temp coefficient
  lcdwrite(COMMAND, 0x14);              // LCD bias mode 1:48: try 0x13 or 0x14
  lcdwrite(COMMAND, 0x20);              // we must send 0x20 before modifying the display control mode
  lcdwrite(COMMAND, 0x0C);              // set display control to normal mode(D=1, E=0)
	while(1)
	{
		Draw();
	}
	return 0;
}
void static lcdwrite(enum typeOfWrite type, char message)
{
  if(type == COMMAND)
  {
    //wait until SSI0 not busy/transmit FIFO empty
	while((SPI0->S & (1 << 5)) != (1 << 5));
	GPIOC->PDOR &= ~(1 << 7);//PTC7(DC) => DC = 0, sending command
    SPI0->D = message;//command out
    //wait until SSI0 not busy/transmit FIFO empty. FIFO not be used when sending commands. The FIFOs are not used when writing
	//commands, and they are used when writing data.  This
	//ensures that the Data/Command pin status matches the byte
	//that is actually being transmitted.
    while((SPI0->S & (1 << 5)) != (1 << 5));
  } 
  else if(type == DATA)
  {
    while((SPI0->S & (1 << 5)) != (1 << 5));//The S register must be read with SPTEF(5th bit in S register) set to 1 before writing data to the D
	//register; otherwise, the D write is ignored.
	GPIOC->PDOR |= (1 << 7);//PTC7(DC) => DC = 0, sending data
    SPI0->D = message;//data out
  }
}

